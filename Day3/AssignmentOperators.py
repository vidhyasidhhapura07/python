# ASSIGNMENT OPERATORS

A=75

# = (Assignment operator)
print(A)

# +=(Plus Equel to operator)
A += 5
print("A is 75 but A+= is A = A + 5 =" ,A)

#  -=(Minus Equel to operator)
A=75
A -= 5
print("A is 75 but a-= is A = A - 5 = ",A)

#  *=(multiplication Equel to operator)
A=75
A *= 5
print("A is 75 but a*= is A = A * 5 = ",A)

#  /=(division Equel to operator)
A=75
A /= 5
print("A is 75 but a/= is A = A / 5 = ",A)

#  %=(Modulus Equel to operator)
A=75
A %= 5
print("A is 75 but a%= is A = A % 5 = ",A)

# **= (Exponentiation Equel to operator)
A=75
A **= 5
print("A is 75 but a**= is A = A ** 5 = ",A)

# //= (Floor division Equel to operator)
A=75
A //= 5
print("A is 75 but a//= is A = A // 5 = ",A)

# &= (Bitwise AND Equel to operator)
A=75
A &= 5
print("A is 75 but a &= is A = A & 5 = ",A)
