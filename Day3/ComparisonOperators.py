#COMPARISON OPERATORS
a=68
b=98

#  == (Equal to operator)
print(a == b) # false

# != (Not equal to operator)
print(a != b) # true

# > (Greater than operator)
print(a > b) # flase

# < (Less than operator)
print(a < b) # true

#  >= (Greater than or equal to operator)
print(a >= b) #false

# <= (Less than or equal to operator)
print(a <= b) #true

#  (chain comparison operators)

A=8
print(1 < A and A < 10) # true
print(1 < A or A > 10) #true
print(1 < A and A > 10) # false