# LOGICAL OPERATOR
X=110

#  and (and operator (when both statements are true Returns True) )
print(X < 200 and X > 100) # true
print(X > 200 and X < 100) # false

# or (or operator (one of the statements is true Returns True ))
print(X < 200 or X < 100) # true
print(X > 200 or X < 100) # false

# not (not operator (returns False if the result is true) )
# Use like A Reverse the result

print(not (X < 200 and X > 100))# flase
